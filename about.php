<h1>About Statgolf</h1>
<p>
Statgolf began in 2003 as an attempt to analyze and improve my golf game.  I started keeping all of my scorecards and would occasionally go back and look
through them.  That lasted for about one season before it became too cumbersome.<br><br>

Next I began recording all of my scores in an excel spreadsheet which was easier.  I was able to analyze my scores and look for trends.  The 
spreadsheet would calculate a handicap with the help of some VBA macros but was very tricky to update and maintain.  As I 
started to play more golf I wanted to be able to enter rounds from every course I played, not just my home course.  Maintaining multiple courses 
proved too complex for a spreadsheet so I decided to create an online application and StatGolf was born.<br><br>

I am constantly adding features.  I have a longer list than I have time but still relish feedback from my users.
Often meaningful suggestions get moved to the top of the list, so don't hesitate to submit your ideas.  Even if your idea does not make the next round
of changes, I promise at least a reply.

<br><br> - Garth McGraw
